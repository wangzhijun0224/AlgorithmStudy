/***********************************************************************************
	选择排序： 从未被排序的整数中找出最小的整数，将其放到已排序整数列表的下一个位置
***********************************************************************************/
void select_sort(int list[], int n)
{
	int tmp, min;

	for (int i = 0; i < n-1; ++i)
	{
		// 从未排序的整数中找出最小的整数
		min = i;
		for (int j = i + 1; j < n; ++j)
		{
			if (list[j] < list[min])
			{
				min = j;
			}
		}

		// swap
		tmp = list[i], list[i] = list[min], list[min] = tmp;
	}
}

